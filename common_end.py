def common_end(arr1, arr2):
    # Check if they have the same first or last element
    return arr1[0] == arr2[0] or arr1[-1] == arr2[-1]

# Example usage
print(common_end([1, 2, 3], [7, 3]))      # Output: True
print(common_end([1, 2, 3], [7, 3, 2]))   # Output: False
print(common_end([1, 2, 3], [1, 3]))      # Output: True
